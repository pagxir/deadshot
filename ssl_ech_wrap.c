    uint8_t *ptlv = NULL, *pech = NULL;
    size_t tlv = 0, outlen = 0;

    if (*data == HANDSHAKE_TYPE_CLIENT_HELLO) {
        *dest++ = *data++;
        ptlv = dest;
        dest += 3;

        tlv = *data++;
        tlv = (tlv << 8) | *data++;
        tlv = (tlv << 8) | *data++;

        pech = data;
        encode_client_hello(ctx, dest, sizeof(hold) - (dest - hold), pech, tlv, &outlen);

        *ptlv++ = (outlen >> 16);
        *ptlv++ = (outlen >> 8);
        *ptlv++ = (outlen >> 0);

        memcpy(ctx->base, hold, outlen + (ptlv - hold));
        return outlen + 4;
    }

